export function getLanguage(): Language {
  return getLocales()[0]?.languageCode === "de" ? "de" : "en";
}

export function getLocale(): string {
  return getLocales()[0]?.languageTag ?? "en-US";
}

export function uses24HourClock(): boolean {
  return getCalendars()[0]?.uses24hourClock ?? getLanguage() === "de";
}

export function t(key: keyof typeof translations.en, vars: Record<string, string | number> = {}): string {
  let value: string = translations[getLanguage()][key] ?? translations.en[key];
  for (const [k, v] of Object.entries(vars)) value = value.replace(`{${k}}`, String(v));
  return value;
}

export function formatClock(d: Date): string {
  return d.toLocaleTimeString(getLocale(), {
    hour: "numeric",
    minute: "2-digit",
    hour12: !uses24HourClock(),
  });
}
